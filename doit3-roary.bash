#! /bin/bash

. $(dirname ${BASH_SOURCE[0]})/doit-preamble.bash

# ------------------------------------------------------------------------

rm -rf ${ROARY}
mkdir -p ${ROARY}

# ------------------------------------------------------------------------
# Run Roary
# ------------------------------------------------------------------------

echo 1>&2 '# Run Roary'
roary -e -mafft -p ${THREADS} -f ${ROARY}/ -r ${INPUTS}/*.gff

# ------------------------------------------------------------------------
# Run FastTreeMP
# ------------------------------------------------------------------------

echo 1>&2 '# Run FastTreeMP'
export OMP_NUM_THREADS=${THREADS}
FastTreeMP -nt -gtr ${ROARY}/*/core_gene_alignment.aln > ${ROARY}/tree.phy

# ------------------------------------------------------------------------
# Done.
# ------------------------------------------------------------------------

echo 1>&2 '# Done.'

